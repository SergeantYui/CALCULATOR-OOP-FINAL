using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace CALCULATOR_OOP_FINAL
{
    public class trigonometry
    {
        public double sinh;
        public double cosh;
        public double tanh;

        public double asin;
        public double acos;
        public double atan;

        public double sin;
        public double cos;
        public double tan;

        public void sinH()
        {
            sinh = Math.Sinh(sinh);
        }
        public void cosH()
        {
            cosh = Math.Sinh(cosh);
        }
        public void tanH()
        {
            tanh = Math.Tanh(tanh);
        }
        public void Asin()
        {
            asin = Math.Asin(asin);
        }
        public void Acos()
        {
            acos = Math.Acos(acos);
        }
        public void Atan()
        {
            atan = Math.Atan(atan);
        }
        public void Sin()
        {
            sin = Math.Sin(sin);
        }
        public void Cos()
        {
            cos = Math.Cos(cos);
        }
        public void Tan()
        {
            tan = Math.Tan(tan);
        }
    }
}
